public class Switchcase {
    public static void main(String[] args) {
        int num = 6;
        switch(num) {
            case 3:
                System.out.println("Three");
                break;
            case 6:
                System.out.print("Six");
            case 9:
                System.out.println("Nine"); 
                break; 
            default:
                System.out.println("Default");
        }
    }
}
